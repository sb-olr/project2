'use strict'

module.exports = class Contact {
    constructor(first_name, last_name, email, bday, tel_nr, notes) {
        this.first_name = first_name;
        this.last_name = last_name;
        this.email = email;
        this.bday = bday;
        this.tel_nr = tel_nr;
        this.notes = notes
    }
}